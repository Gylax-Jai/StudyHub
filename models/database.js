const mongoose = require("mongoose");

const studyschema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    trim: true
  },
  email: {
    type: String,
    required: true,
    unique: true,
    lowercase: true
  },
  password: {
    type: String,
    required: true
  }
});

const study = mongoose.model("study", studyschema);
module.exports = study;
